      <article id="about">
        <p class="aboutus" data-animate-scroll='{"x": "20", "alpha": "0", "duration": "1.5"}'>SELAMAT DATANG DI SITUS RESMI PT AN NAMIROH TRAVELINDO</p>
        <p class="keunggulan" data-animate-scroll='{"x": "-20", "alpha": "0", "duration": "1.5"}'>KBIH An Namiroh merupakan salah satu kelompok bimbingan Ibadah Haji di Kecamatan
          Mojosari Kabupaten Mojokerto dengan akta pendirian nomer 4 tanggal 3 April 2001 yang telah ditunjuk oleh Departemen Agama RI.</p>
        <p class="keunggulan" data-animate-scroll='{"x": "20", "alpha": "0", "duration": "1.5"}'>KBIH An Namiroh adalah badan usaha yang bergerak di bidang biro pemberangkatan Haji Reguler dan Haji Plus, 
          Umroh Reguler dan Umroh Plus. Kami memiliki pelayanan yang meliputi, Manasik (bimbingan) Ibadah Haji dan Umroh secara modern 
          (Audio dan Visual Digital), pengurusan Pasport, Visa, persiapan & perlengkapan Haji, Guide (pembimbing) bersekala International mulai dari pemberangkatan hingga pemulangan Haji ke tanah air.</p>
        <p class="keunggulan" data-animate-scroll='{"x": "-20", "alpha": "0", "duration": "1.5"}'>KBIH An Namiroh telah berjalan selama 12 tahun dan dalam perjalananya Alhamdulillah Jama’ah yang sudah bergabung di KBIH An Namiroh ini merasakan kepuasan dalam bimbingan dan pelayanan. 
          Meskipun KBIH An Namiroh berdomisili di Kabupaten Mojokerto, namun dalam prakteknya banyak Jama’ah dari luar Kabupaten Mojokerto yang ikut bergabung dalam pemberangkatan Haji dan Umroh bersama kami.</p>
